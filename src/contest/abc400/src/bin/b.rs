fn main() {
    input! {
        n:u128, m: usize
    }

    let mut ans = 1_u128;
    let mut plus = 1_u128;
    for i in 0..m {
        plus *= n;
        if ans + plus > 1000_000_000 {
            println!("inf");
            return;
        }
        ans += plus;
    }

    println!("{}", ans);
}
